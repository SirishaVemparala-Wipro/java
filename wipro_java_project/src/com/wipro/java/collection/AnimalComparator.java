package com.wipro.java.collection;
class AnimalComparator {

	    private String name;   
	    private int age;      
	    //Constructor to initialize movie details.    
	    public AnimalComparator(String name,int age) {
	        this.name = name;
	        this.age= age;
	    }
	    //getter methods
	    public String getName() {
	        return name;
	    }
	    public int getAge() {
	        return age;
	    }
	}
